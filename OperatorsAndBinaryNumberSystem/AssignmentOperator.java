package OperatorsAndBinaryNumberSystem;

public class AssignmentOperator {
    public static void main(String[] args) {
        //addition
        int a=10,b=5;
        System.out.println(a+b);
        //subtraction
        System.out.println(a-b);
        //multiply
        System.out.println(a*b);
        //divide
        System.out.println(a/b);
        //mod
        System.out.println(a%b);

    }
}
