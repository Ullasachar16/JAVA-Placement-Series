package OperatorsAndBitManipulation;

public class RelationalOperator {
    public static void main(String[] args) {
        //equal to
        int a=10,b=5;
        System.out.println(a==b);

        //not equal to
        System.out.println(a!=b);

        //greater
        System.out.println(a>b);

        //smaller
        System.out.println(a<b);

        //greater than equal to
        System.out.println(a>=b);

        //smaller than equal to
        System.out.println(a<=b);


    }
}
